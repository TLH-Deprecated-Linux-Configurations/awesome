local dpi = require('beautiful').xresources.apply_dpi
-- ########################################################################
-- ########################################################################
-- ########################################################################
-- @usage
-- lib.widgets.rounded(dpi(10))
return function(size)
    if size == nil then
        size = dpi(12)
    end
    return function(c, w, h)
        gears.shape.rounded_rect(c, w, h, size)
    end
end
