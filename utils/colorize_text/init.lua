function colorize_text(txt, fg)
    if fg ~= nil then
        return "<span foreground='" .. fg .. "'>" .. txt .. '</span>'
    else
        return "<span foreground='#f4f4f7'>" .. txt .. '</span>'
    end
end
return colorize_text
